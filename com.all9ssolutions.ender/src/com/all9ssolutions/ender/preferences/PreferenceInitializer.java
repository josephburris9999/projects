package com.all9ssolutions.ender.preferences;

import static com.all9ssolutions.ender.Messages.ENDER_inline_label;
import org.eclipse.core.runtime.preferences.AbstractPreferenceInitializer;
import org.eclipse.jface.preference.IPreferenceStore;

import com.all9ssolutions.ender.Activator;

/**
 * This class is used to initialize default preference values.
 * 
 * @see org.eclipse.core.runtime.preferences.AbstractPreferenceInitializer
 */
public class PreferenceInitializer extends AbstractPreferenceInitializer implements PreferenceConstants {

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.core.runtime.preferences.AbstractPreferenceInitializer#
	 * initializeDefaultPreferences()
	 */
	public void initializeDefaultPreferences() {
		IPreferenceStore store = Activator.getDefault().getPreferenceStore();
		store.setDefault(PREF_COMMENT_TYPE, ENDER_inline_label);
	}// end initializeDefaultPreferences
}// end class
